#!/bin/bash

add() {
    if [ $# -ne 2 ]; then
        echo "Ошибка: Функция add требует ровно два аргумента"
        exit 1
    fi


    if ! [[ "$1" =~ ^[0-9]+(\.[0-9]+)?$ && "$2" =~ ^[0-9]+(\.[0-9]+)?$ ]]; then
        echo "Ошибка: Аргументы должны быть числами"
        exit 1
    fi


    sum=$(echo "$1 + $2" | bc)
    echo "Сумма: $sum"
}


if [ $# -ne 2 ]; then
    echo "Использование: $0 <число1> <число2>"
    exit 1
fi

add "$1" "$2"
